const interp = require('interpolate-arrays');

export const addColors = (data:number[][], min:number, max:number, x:number ) => {   
    if(!data) return;  
    if(x<min) x = min;
    if(x>max) x = max;
    if(min == max) return [0,0,0];
    const xn = (x-min)/(max-min);
    return interp(data, xn);
}

